package com.bjtu.domain;

public class Roles {
    private int id;
    private ERole name;

    public Roles(int id, ERole name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public ERole getName() {
        return name;
    }

    public void setName(ERole name) {
        this.name = name;
    }
}
